:var object-count

: level! ( -- )
	# clear objects
	255 for
		0 0 0 0 i >sprite
		i sprite-id i + !
		0 i kind!
	next
	1 object-count !
	
	# set GP
	level-number @ level-count mod
	levels + @ GP !
	level-number inc@
	
	# find/init objects
	29 for
		39 for
			i j tile-grid@ @
			dup 4 = if # player start point
				16x24 0
				i 8 * j 1 - 8 * player >sprite
				player-kind player kind!
			then
			dup 5 = if # block
				16x24 7
				i 8 * j 1 - 8 * object-count @ >sprite
				block-kind object-count @ kind!
				object-count inc@
			then
			dup 2 = if # block target
				16x16 invisible 0
				i 8 * j 8 * object-count @ >sprite
				target-kind object-count @ kind!
				object-count inc@
			then
			drop
		next
	next
;

: sort-sprites ( -- )
	15 for
		i
		i for
			dup py i py <
			if drop i then
		next

		# swap mappings in the id table:
		dup sprite-id indexof
		  i sprite-id indexof swap@

		# swap entries in the kind table:
		dup object-kind +
		  i object-kind + swap@

		# swap sprite registers:
		sprite@ i sprite@
		3 for
			over i +
			over i +
			swap@
		next
		2drop
	next
;

: has-block? ( x y -- flag )
	false -rot
	15 for
		2dup i c-sprite? i kind? block-kind = and >r
		rot r> or -rot
	next
	2drop
;

: blocked? ( dx2 dy2 dx1 dy1 -- flag )
	player py + >r player px + >r
	i j c-tile? if rdrop rdrop 2drop true exit then
	i j has-block? if
		swap r> + swap r> +
		2dup c-tile? -rot
		has-block? or
	else
		rdrop rdrop 2drop false
	then
;

:var pblock
: push ( dx dy x y -- )
	player py + swap player px + swap
	15 for
		2dup i c-sprite? i kind? block-kind = and
		if
			i pblock ! >r >r
			over pblock @ px + pblock @ px!
			dup  pblock @ py + pblock @ py!
			r> r>
		then
	next
	2drop
	player py + player py!
	player px + player px!
;

